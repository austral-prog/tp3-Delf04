def check_vowels():
    nombre = input("> ").lower()
    vocalA = f"Contiene a: {"a" in nombre}"
    vocalE = f"Contiene e: {"e" in nombre}"
    vocalI = f"Contiene i: {"i" in nombre}"
    vocalO = f"Contiene o: {"o" in nombre}"
    vocalU = f"Contiene u: {"u" in nombre}"
    print(vocalA)
    print(vocalE)
    print(vocalI)
    print(vocalO)
    print(vocalU)
