def check_vowels():
    nombre = input("> ")
    nombre_minuscula = nombre.lower()
    print(f"Contiene: {"a" in nombre_minuscula}")
    print(f"Contiene: {"e" in nombre_minuscula}")
    print(f"Contiene: {"i" in nombre_minuscula}")
    print(f"Contiene: {"o" in nombre_minuscula}")
    print(f"Contiene: {"u" in nombre_minuscula}")
